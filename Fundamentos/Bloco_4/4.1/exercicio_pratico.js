//1. Criação de cinco programas, um para cada operação aritmética básica, com duas variáveis, A e B.
let a = 10;
let b = 5;

//Adição
if (a > b) {
    console.log(a + b);
}

//Subtração
if (a > b) {
    console.log(a - b);
}

//Multiplicação
if (a > b) {
    console.log(a * b);
}

//Divisão
if (a > b) {
    console.log(a / b);
}

//Módulo
if (a > b) {
    console.log(a % b);
}

//2. Criação de um programa que retorne o maior de dois números.
if (a > b) {
    console.log(a)
} else {
    console.log(b)
}
